#Define a list of names
names = ['Mrudula', 'Satya', 'Vamsi']
#Loop through the names and print
print('Printing all the names in the list')
for name in names:
  print(name)
#Print all the names in the list in reverse order
print('Printing all the names in reverse order from the list')
names.reverse();
for name in names:
  print(name)
